<?php
// İfade ve Operatörler/ Aritmetik Operatörler / Değer Atama Operatörleri

// İki sayinin toplamini bulma
$sayi1 = 10;
$sayi2 = 5;
$toplam = $sayi1 + $sayi2;
echo "Toplam: " . $toplam . "\n";

// İki sayinin farkini bulma
$fark = $sayi1 - $sayi2;
echo "Fark: " . $fark . "\n";

// İki sayinin çarpimini bulma
$carpim = $sayi1 * $sayi2;
echo "Çarpim: " . $carpim . "\n";

// İki sayinin bölümünü bulma
$bolum = $sayi1 / $sayi2;
echo "Bölüm: " . $bolum . "\n";

// İki sayinin bölümünden kalani bulma (mod işlemi)
$kalan = $sayi1 % $sayi2;
echo "Kalan: " . $kalan . "\n";

// Bir sayinin negatifini alma
$negatif = -15;
echo "-15 in Negatifi: " . -$negatif . "\n";
?>